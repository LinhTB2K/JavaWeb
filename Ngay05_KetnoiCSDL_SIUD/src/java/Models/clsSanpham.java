package Models;

/**
 *
 * @author Admin
 */
public class clsSanpham {
    int id;
    String tensp;
    float gia;
    boolean trangthai;

    public clsSanpham() {
    }

    public clsSanpham(int id, String tensp, float gia, boolean trangthai) {
        this.id = id;
        this.tensp = tensp;
        this.gia = gia;
        this.trangthai = trangthai;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTensp() {
        return tensp;
    }

    public void setTensp(String tensp) {
        this.tensp = tensp;
    }

    public float getGia() {
        return gia;
    }

    public void setGia(float gia) {
        this.gia = gia;
    }

    public boolean isTrangthai() {
        return trangthai;
    }

    public void setTrangthai(boolean trangthai) {
        this.trangthai = trangthai;
    }
    
}
